using UnityEngine;
using System.Collections;

public class AngleDir : MonoBehaviour {

	// Used to get dirNum, the direction of the target
	public float getDir(Vector3 fwd, Vector3 targetDir, Vector3 up) {
		Vector3 perp = Vector3.Cross(fwd, targetDir);
		float dir = Vector3.Dot(perp, up);		
		if (dir > 0f) {
			return 1f;
		} else if (dir < 0f) {
			return -1f;
		} else {
			return 0f;
		}
	}	
}